    public void fillTable() {
        this.model = (DefaultTableModel) tblKhachHang.getModel();
        this.model.setRowCount(0);
        for (KhachHang x : this.listKhachHang) {
            Object[] row = {
                x.getHo_ten(), x.getSo_dien_thoai(), x.getGioi_tinh() ? "Nam" : "Nữ", x.getDiaChi(), x.getNgay_tao()
            };
            this.model.addRow(row);
        }
    }

    void showData(int index) {
        KhachHang kh = this.listKhachHang.get(index);
        txtTen.setText(kh.getHo_ten());
        txtDiaChi.setText(kh.getDiaChi() == null ? "" : kh.getDiaChi());
        txtSdt.setText(kh.getSo_dien_thoai() == null ? "" : kh.getSo_dien_thoai());
        lblNgaytao.setText(String.valueOf(kh.getNgay_tao()));
        lblNgaysua.setText(String.valueOf(kh.getNgay_sua()));
        if (kh.getGioi_tinh()) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }

    public KhachHang getKH() {
        int id = this.listKhachHang.get(tblKhachHang.getSelectedRow()).getId();
        KhachHang kh = this.service.getKhachHangById(id);
        kh.setHo_ten(txtTen.getText());
        kh.setSo_dien_thoai(txtSdt.getText());
        kh.setGioi_tinh(rdoNam.isSelected() ? true : false);
        kh.setDiaChi(txtDiaChi.getText());
        kh.setNgay_tao(new Date());
        kh.setNgay_sua(new Date());
        kh.setXoa_mem(0);
        return kh;
    }

    public KhachHang xoaKh() {
        int id = this.listKhachHang.get(tblKhachHang.getSelectedRow()).getId();
        KhachHang kh = this.service.getKhachHangById(id);
        kh.setXoa_mem(1);
        return kh;
    }

