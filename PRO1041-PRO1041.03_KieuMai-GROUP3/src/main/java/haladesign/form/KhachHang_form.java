    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (tblKhachHang.getSelectedRow() < 0) {
            Notification notification = new Notification(this.main, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Chưa chọn khách hàng nào!");
            notification.showNotification();
            return;
        }
        if (service.insert(xoaKh())) {
//            JOptionPane.showMessageDialog(this, "Xóa thành công");
            new Notification(this.main, Notification.Type.SUCCESS, Notification.Location.TOP_RIGHT, "Xóa thành công").showNotification();
            this.listKhachHang = this.service.getKH();
            fillTable();
        } else {
//            JOptionPane.showMessageDialog(this, "Xóa thất bại");
            new Notification(this.main, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Xóa không thành công").showNotification();
        }

    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (tblKhachHang.getSelectedRow() < 0) {
            Notification notification = new Notification(this.main, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Chưa chọn khách hàng nào!");
            notification.showNotification();
            return;
        }
        if (service.insert(getKH())) {
//            JOptionPane.showMessageDialog(this, "Update thành công");
            new Notification(this.main, Notification.Type.SUCCESS, Notification.Location.TOP_RIGHT, "Update thành công").showNotification();
            this.listKhachHang = this.service.getKH();
            fillTable();
        } else {
//            JOptionPane.showMessageDialog(this, "Update thất bại");
            new Notification(this.main, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Update không thành công").showNotification();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

