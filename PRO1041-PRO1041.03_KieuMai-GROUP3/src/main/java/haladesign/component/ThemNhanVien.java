    public void loadRoleToCbo() {
        cboRole.removeAll();
        cboRole.setModel((DefaultComboBoxModel) cbbModel);
        for (QuyenHan role : quyenHanService.getList()) {
            cbbModel.addElement(role);
        }
    }

    public boolean validateEmplData() {
        if (txtFullname.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "Tên nhân viên không được để trống");
            return false;
        } else if (!txtPhonenum.getText().trim().matches("^(0[0-9]{9}|84[0-9]{9})$")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ");
            return false;
        } else if (cboRole.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Chỉ có thể có 1 chủ cửa hàng");
            return false;
        }

        return true;
    }
    
