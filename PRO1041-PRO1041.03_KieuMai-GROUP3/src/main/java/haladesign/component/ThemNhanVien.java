    public NhanVien getDataFromFields() {
        if (validateEmplData()) {
            NhanVien empl = new NhanVien();
            empl.setFullName(txtFullname.getText());
            empl.setPhoneNum(txtPhonenum.getText());
            empl.setNote(txtNote.getText());
            empl.setRole((QuyenHan) cboRole.getSelectedItem());
            return empl;
        } else {
            return null;
        }

    }

    public void handleSubmit() {
        NhanVien newNhanVien = getDataFromFields();
        if (newNhanVien != null) {
            newNhanVien.setUserState("Đã gửi lời mời ");
            if (this.nhanVienService.addNewNhanVien(newNhanVien)) {
                System.out.println(newNhanVien);
                ThemMoiNhanVienThanhCong msg = new ThemMoiNhanVienThanhCong(this.main, newNhanVien);
                this.main.showForm(msg);
            }
        }
    }

